public class NumbersDemo {
    public static void main (String args[]) {
        int n = 2;
        System.out.println("The number is: " + n);
    }
    public static void displayTwiceTheNumber(int n) {
        System.out.println("Twice the number is: " + n * 2);
    }

    public static void displayNumberPlusFive(int n) {
        System.out.println("The number plus five is: " + n + 5);
    }

    public static void displayNumberSquared(int n) {
        System.out.println("The number squared is: " + n * n);

    }
}